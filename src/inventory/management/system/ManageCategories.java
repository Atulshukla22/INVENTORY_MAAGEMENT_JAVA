    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
         String Name = txtName.getText();
        if(ValidateFields()){
            JOptionPane.showMessageDialog(null, "All fields arr required");
        } 
        else{
            try {
                Connection con = ConnectionProvider.getcon();
                PreparedStatement ps = con.prepareStatement("insert into category(name) values(?)");
                ps.setString(1, Name);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "new Category added successfully");
                setVisible(false);
                new ManageCategories().setVisible(true);
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                
            }
            
        }
        

    }//GEN-LAST:event_btnSaveActionPerformed

