    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String Name = txtName.getText();
        String MobileNumber = txtMobileNumber.getText();
        String Email = txtEmail.getText();
        String Address = txtAddress.getText();
        String Password = txtPassword.getText();
        String Status = (String) ComboBoxstatus.getSelectedItem();
        
        if (ValidateFields("new")) {
            JOptionPane.showMessageDialog(null, "All fields arr required");
        } else {
            try {
                Connection con = ConnectionProvider.getcon();
                PreparedStatement ps = con.prepareStatement("insert into application_user(userRole,name,mobileNumber,email,address,password,status) values('admin',?,?,?,?,?,?)");
                ps.setString(1, Name);
                ps.setString(2, MobileNumber);
                ps.setString(3, Email);
                ps.setString(4, Address);
                ps.setString(5, Password);
                ps.setString(6, Status);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "new user added successfully");
                setVisible(false);
                new ManageUser().setVisible(true);
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                
            }
            
        }
        

    }//GEN-LAST:event_btnSaveActionPerformed

