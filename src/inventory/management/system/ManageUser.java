    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) TableUser.getModel();
        try {
            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select* from application_user where userRole ='admin'");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("appuser"), rs.getString("name"), rs.getString("MobileNumber"), rs.getString("email"), rs.getString("address"), rs.getString("status")});
            }
            
        } catch (Exception e) {
        }
        
        btnUpdate.setEnabled(false);
        

    }//GEN-LAST:event_formComponentShown

