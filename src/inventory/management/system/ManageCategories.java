    private void catTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_catTableMouseClicked
        // TODO add your handling code here:
         int index = catTable.getSelectedRow();
        TableModel model = catTable.getModel();
        
         String id = model.getValueAt(index, 0).toString();
        categoryPK = Integer.parseInt(id);
        
        String name = model.getValueAt(index, 1).toString();
        txtName.setText(name);
        
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_catTableMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String Name = txtName.getText();
        if(ValidateFields()){
            JOptionPane.showMessageDialog(null, "All fields arr required");
        } 
        else{
            try {
                Connection con = ConnectionProvider.getcon();
                PreparedStatement ps = con.prepareStatement("update category set name=? where category=?");
                ps.setString(1, Name);
                ps.setInt(2, categoryPK);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, " Category updated successfully");
                setVisible(false);
                new ManageCategories().setVisible(true);
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                
            }
            
        }
        
          
        
    }//GEN-LAST:event_btnUpdateActionPerformed

